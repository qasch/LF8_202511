public class Schleifen {
    public static void main(String[] args) {
        System.out.println("while Loop: Zahlen von 10 bis 1:");
       int i = 10;
       // while(bedingung)
       while(i >= 1){
           System.out.println(i);
           i--;    // i = i - 1
       }
        System.out.println("for Loop: Zahlen von 1 bis 10:");
        //for(index; bedingung; inkrement/dekrement)
        for(int j = 1; j <= 10; j++){
            System.out.println(j);
        }
    }
}


